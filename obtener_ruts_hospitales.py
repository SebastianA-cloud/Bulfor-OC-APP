def mp_get(params, max_reintentos=5):
    params = dict(params)
    params['ticket'] = MP_TICKET
    espera = 3
    for intento in range(max_reintentos):
        r = requests.get(MP_BASE, params=params, timeout=30)
        if r.status_code == 429:
            print(f"    ⏳ Esperando {espera}s por límite de la API...")
            time.sleep(espera)
            espera *= 2
            continue
        r.raise_for_status()
        return r.json()
    raise requests.exceptions.HTTPError("429 persistente")


def main():
    print(f"Consultando RUT de {len(HOSPITALES_CODIGOS)} hospitales...\n")
    filas = []

    for i, (hospital, codigo) in enumerate(HOSPITALES_CODIGOS.items(), 1):
        print(f"[{i}/{len(HOSPITALES_CODIGOS)}] {hospital}")
        try:
            data = mp_get({"codigo": codigo})
            listado = data.get("Listado")
            if not listado:
                print("   ⚠ Sin datos")
                continue
            comprador = listado[0].get("Comprador", {})
            rut = comprador.get("RutUnidad")
            region = comprador.get("RegionUnidad")
            comuna = comprador.get("ComunaUnidad")
            print(f"   ✔ RUT: {rut}")
            filas.append({
                "nombre_original": hospital,
                "rut": rut,
                "region": region,
                "comuna": comuna,
                "codigo_oc_ejemplo": codigo,
            })
        except Exception as e:
            print(f"   ✗ Error: {e}")
        time.sleep(1.5)  # pausa conservadora entre consultas

    if filas:
        supabase.table("hospitales_maestro").upsert(filas, on_conflict="nombre_original").execute()
        print(f"\n✔ {len(filas)} hospitales guardados en hospitales_maestro")
    else:
        print("\n⚠ No se guardó ningún hospital")


if __name__ == "__main__":
    main()
